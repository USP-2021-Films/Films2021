package com.example.filmchoice;

public class FilmArray {
    private String info, value;

    public FilmArray(String info, String value) {
        this.info = info;
        this.value = value;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
